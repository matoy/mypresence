package i18n

import "net/http"

// LangInfo holds metadata about a supported language.
type LangInfo struct {
	Code  string
	Label string
	Flag  string
}

// Supported languages in display order.
var Supported = []LangInfo{
	{"en", "English", "🇺🇸"},
	{"fr", "Français", "🇫🇷"},
	{"de", "Deutsch", "🇩🇪"},
	{"es", "Español", "🇪🇸"},
	{"it", "Italiano", "🇮🇹"},
}

// T returns the translation map for the given language code.
// Falls back to English if the language is not found.
func T(lang string) map[string]string {
	if m, ok := all[lang]; ok {
		return m
	}
	return all["en"]
}

// LangFromRequest resolves the language to use for a request:
// 1. If a "lang" cookie is set and valid, use it.
// 2. Otherwise use defaultLang (from config).
// 3. Falls back to "en".
func LangFromRequest(r *http.Request, defaultLang string) string {
	if c, err := r.Cookie("lang"); err == nil {
		for _, s := range Supported {
			if s.Code == c.Value {
				return c.Value
			}
		}
	}
	for _, s := range Supported {
		if s.Code == defaultLang {
			return defaultLang
		}
	}
	return "en"
}

